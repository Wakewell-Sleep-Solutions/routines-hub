// W14: Stale Branches detector (daily).
// Queries GitHub GraphQL for every branch in a repo, filters to non-default
// branches whose last commit is >30 days old AND don't have an open PR.
// Posts a rolling Linear issue per repo summarizing stale branches.
//
// Signature: [stale-branches:<repo>]  — one rolling issue per repo.
// Dedup: ensureIssue → startsWith title match + open-state filter. Heartbeat
//        on match; G1 CHECK 4 auto-closes after 3d of silence.

import { ensureIssue, commentOnIssue } from '../lib/linear.mjs';

const { TARGET_REPO, GITHUB_TOKEN } = process.env;
if (!TARGET_REPO) throw new Error('TARGET_REPO required');
if (!GITHUB_TOKEN) throw new Error('GITHUB_TOKEN required (repo read scope)');

const OWNER = 'Wakewell-Sleep-Solutions';
const STALE_DAYS = 30;
const THRESHOLD_COUNT = 3;

// Branch refs + last-committed dates + default branch name, one query.
async function gh(query, variables = {}) {
  const res = await fetch('https://api.github.com/graphql', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${GITHUB_TOKEN}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ query, variables }),
  });
  const json = await res.json();
  if (json.errors) throw new Error(`GitHub GraphQL error: ${JSON.stringify(json.errors)}`);
  return json.data;
}

// Note: GitHub's GraphQL `Commit.associatedPullRequests` does NOT accept a
// `states` argument (unlike Repository.pullRequests). So we fetch up to 5
// associated PRs with their `state` field and filter OPEN ones client-side.
const data = await gh(
  `query Branches($owner: String!, $name: String!, $after: String) {
    repository(owner: $owner, name: $name) {
      defaultBranchRef { name }
      refs(refPrefix: "refs/heads/", first: 100, after: $after) {
        pageInfo { hasNextPage endCursor }
        nodes {
          name
          target { ... on Commit {
            committedDate
            oid
            associatedPullRequests(first: 5) {
              nodes { state }
            }
          } }
        }
      }
    }
  }`,
  { owner: OWNER, name: TARGET_REPO },
);

const repo = data.repository;
if (!repo) {
  console.log(`${TARGET_REPO}: repo not found via GraphQL — skipping.`);
  process.exit(0);
}

const defaultBranch = repo.defaultBranchRef?.name;
const now = Date.now();
const threshold = now - STALE_DAYS * 86400000;

const staleBranches = [];
for (const ref of repo.refs.nodes) {
  if (ref.name === defaultBranch) continue;
  const ts = new Date(ref.target.committedDate).getTime();
  if (ts > threshold) continue;
  // Skip branches with any OPEN PR (they're not abandoned, just slow)
  const prs = ref.target.associatedPullRequests?.nodes || [];
  if (prs.some((p) => p.state === 'OPEN')) continue;
  staleBranches.push({
    name: ref.name,
    committedDate: ref.target.committedDate,
    days: Math.floor((now - ts) / 86400000),
    sha: ref.target.oid.slice(0, 7),
  });
}

staleBranches.sort((a, b) => b.days - a.days);

if (staleBranches.length < THRESHOLD_COUNT) {
  console.log(`${TARGET_REPO}: ${staleBranches.length} stale branches — below threshold ${THRESHOLD_COUNT}, no issue.`);
  process.exit(0);
}

const signature = `[stale-branches:${TARGET_REPO}]`;
const title = `${signature} ${staleBranches.length} branches untouched ≥${STALE_DAYS}d (no open PR)`;

const sampleRows = staleBranches.slice(0, 20).map(
  (b) => `| \`${b.name}\` | ${b.committedDate.slice(0, 10)} | ${b.days}d | \`${b.sha}\` |`,
);

const sections = [
  `**Repo:** ${TARGET_REPO}`,
  `**Stale branches (>${STALE_DAYS}d, no open PR):** ${staleBranches.length}`,
  `**Default branch:** \`${defaultBranch}\``,
  '',
  '| Branch | Last commit | Age | SHA |',
  '|---|---|---|---|',
  ...sampleRows,
];

if (staleBranches.length > 20) {
  sections.push('');
  sections.push(`_…and ${staleBranches.length - 20} more._`);
}

sections.push('');
sections.push('### Remediation');
sections.push('1. Confirm each branch is actually abandoned (not someone\'s long-running feature)');
sections.push('2. If merged-and-forgotten: `git push origin --delete <branch>`');
sections.push('3. If never-merged-orphan: archive via `git branch -m <name> archive/<name>` or delete after confirming with author');
sections.push('');
sections.push('---');
sections.push(
  `_Auto-generated by routines-hub W14 (stale-branches). Daily scan. Threshold: ≥${THRESHOLD_COUNT} stale branches. Auto-closes via G1 when below threshold for 3d._`,
);

try {
  const result = await ensureIssue({
    signature,
    onMatch: async (existing) => {
      await commentOnIssue(
        existing.id,
        `Still present in ${TARGET_REPO} as of ${new Date().toISOString()}.\n` +
          `Current: **${staleBranches.length} stale branches** (oldest: ${staleBranches[0].days}d — \`${staleBranches[0].name}\`).`,
      );
    },
    onNew: () => ({
      title,
      description: sections.join('\n'),
      projectKey: 'code-health',
      labelNames: ['routine'],
      priority: 4, // Low — hygiene, not blocking
    }),
  });
  console.log(`  ${result.action}: ${result.issue.identifier} — ${signature}`);
} catch (e) {
  console.error(`  ERROR on ${signature}: ${e.message}`);
  process.exit(1);
}
